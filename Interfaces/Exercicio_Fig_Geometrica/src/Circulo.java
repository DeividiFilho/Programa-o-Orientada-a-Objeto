public class Circulo implements IFiguraPlana {
    private double raio;

    public Circulo(double raio) {
        this.raio = raio;
    }

    public double getRaio() {
        return raio;
    }

    public void setRaio(double raio) {
        this.raio = raio;
    }

    @Override
    public double calcularArea() {
        return Math.pow(this.getRaio(), 2)*Math.PI;
    }
}
